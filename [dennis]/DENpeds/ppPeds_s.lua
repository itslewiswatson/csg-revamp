local thePedsE = {}
for i=1,#peds do
	local thePed = createPed ( peds[i][1], peds[i][2], peds[i][3], peds[i][4] )
	setElementFrozen ( thePed, true )
	setPedRotation ( thePed, peds[i][7] )
	setElementInterior( thePed, peds[i][5] )
	setElementDimension ( thePed, peds[i][6] )
	setElementData( thePed, "showModelPed", true, false )
	table.insert(thePedsE,thePed)
end

function getShowModelPed(dim,int)
	for k,v in pairs(thePedsE) do
		if getElementDimension(v) == dim then
			if getElementInterior(v) == int then
				return v
			end
		end
	end
	return false
end

